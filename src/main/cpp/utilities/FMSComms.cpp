#include <frc/smartdashboard/SmartDashboard.h>
#include <frc/DriverStation.h>
#include <frc/Timer.h>
#include <math.h>
#include <utilities/FMSComms.h>
#include <iostream>

FMSComms::FMSComms(){
    if(frc::DriverStation::GetAlliance() != frc::DriverStation::kInvalid) {
        FMSComms::GetAlliance();
    }
}

frc::DriverStation::Alliance FMSComms::GetAlliance(){
    alliance = frc::DriverStation::GetAlliance();
    return alliance;
}

std::string FMSComms::GetAllianceString(frc::DriverStation::Alliance AllianceGet()) {
    if (GetAlliance() == frc::DriverStation::Alliance::kRed) {
        return "Red";
    } else if (GetAlliance() == frc::DriverStation::Alliance::kBlue) {
        return "blue";
    } else {
        return "invalid";
    }
}

double FMSComms::DisplayMatchTime() {
    return frc::SmartDashboard::PutNumber("Match Time", frc::Timer::GetMatchTime().value());
}

